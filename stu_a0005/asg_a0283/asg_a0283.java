class asg_a0283{
public static void main(String args[])
{
int i,j,k,l,m,n;
for(i=5;i>=1;i--)
{
for(j=5;j>i;j--)
{
System.out.print(" ");
}
for(k=1;k<2*i;k++)
{
System.out.print("*");
}
System.out.println();

}
for(l=2;l<6;l++)
{
for(m=5;m>l;m--)
{
System.out.print(" ");
}
for(n=1;n<=2*l-1;n++)
{
System.out.print("*");
}
System.out.println();
}
}
}

